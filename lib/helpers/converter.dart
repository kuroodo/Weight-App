double poundToKg(double pounds) {
  return pounds * 0.45359237;
}

double kgToPound(double kg) {
  return kg * 2.20462262185;
}

double inchToCm(double inches) {
  return inches * 2.54;
}

double cmToInch(double cm) {
  return cm / 2.54;
}

double cmtoMeter(double cm) {
  return cm * 0.01;
}

double meterTocm(double m) {
  return m / 0.01;
}
